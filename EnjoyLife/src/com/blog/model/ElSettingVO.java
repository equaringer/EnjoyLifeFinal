package com.blog.model;

public class ElSettingVO {
	private String blogPrefix ="";
	private String blogSerials="";

	public String getBlogPrefix() {
		return blogPrefix;
	}
	public void setBlogPrefix(String blogPrefix) {
		this.blogPrefix = blogPrefix;
	}
	public String getBlogSerials() {
		return blogSerials;
	}
	public void setBlogSerials(String blogSerials) {
		this.blogSerials = blogSerials;
	}
	
}
