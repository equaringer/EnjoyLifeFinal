package com.blog.model;

public class BlogTagsVO {
	private String postNo;
	private String tagMemberId;
	
	public BlogTagsVO() {

	}

	public String getPostNo() {
		return postNo;
	}

	public void setPostNo(String postNo) {
		this.postNo = postNo;
	}

	public String getTagMemberId() {
		return tagMemberId;
	}

	public void setTagMemberId(String tagMemberId) {
		this.tagMemberId = tagMemberId;
	}
	
}
